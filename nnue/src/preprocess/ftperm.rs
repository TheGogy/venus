use crate::arch::{FEATURES, L1_LEN, NB_INPUT_BUCKETS, PAIRWISE_LEN, QuantNNUEData, USE_FTPERM};

impl QuantNNUEData {
    /// Permute the features to maximize pairwise activations within each block (and thus reduce the
    /// total number of blocks activated)
    pub fn ftperm(&mut self) {
        if !USE_FTPERM {
            return;
        }

        let mut feature_buf = [0i16; L1_LEN];
        for bkt in 0..NB_INPUT_BUCKETS {
            for feat in 0..FEATURES {
                let offset = (bkt * (L1_LEN * FEATURES)) + (feat * L1_LEN);
                feature_buf.copy_from_slice(&self.ftw[offset..offset + L1_LEN]);

                for (ix, iy) in FTPERM_INDICES.iter().copied().enumerate() {
                    self.ftw[offset + ix] = feature_buf[iy];
                    self.ftw[offset + ix + PAIRWISE_LEN] = feature_buf[iy + PAIRWISE_LEN];
                }
            }
        }

        let ftb_unperm = self.ftb;
        for (ix, iy) in FTPERM_INDICES.iter().copied().enumerate() {
            self.ftb[ix] = ftb_unperm[iy];
            self.ftb[ix + PAIRWISE_LEN] = ftb_unperm[iy + PAIRWISE_LEN];
        }

        let l1w_unperm = self.l1w;
        for (ix, iy) in FTPERM_INDICES.iter().copied().enumerate() {
            self.l1w[ix] = l1w_unperm[iy];
            self.l1w[ix + PAIRWISE_LEN] = l1w_unperm[iy + PAIRWISE_LEN];
        }
    }
}

